//=--------------------------------------------------------------------------=
// URL Recognition class
//=--------------------------------------------------------------------------=
class CUrlRec
{
public:
    CUrlRec()  {};
    ~CUrlRec() {};

	HRESULT	HrIdentifyUrls(LPCTSTR cszText, LPINT rgnUrlBounds, LPINT pnUrlNum);
	BOOL	bLaunchUrl(HWND hwndParent, LPCTSTR cszUrl, BOOL bNewBrowser = TRUE);

private:
	BOOL	bUrlNeedsNewBrowser(LPCTSTR cszUrl, LPTSTR szExePath);
	BOOL	bIsUrlPrefix(LPCTSTR cszUrlPrefix, BOOL *pbForBrowser);
	BOOL	bIsUrlSuffix(LPCTSTR cszUrlSuffix);
	BOOL	bIsUrlChar(const UCHAR pch) { const BYTE fb = bLegalForURL[pch];
										  return fb && (fb & 0x01); }
	HRESULT	HrCanonicalizeUrl(LPCTSTR cszURL, LPDWORD pdwURLLength, LPTSTR *pszCanonURL);
	HRESULT	HrAddUrl(LPCTSTR cszUrl, int iStart, short nLength);
	LPCTSTR	FindUrlEnd(LPCTSTR cszColon);
	LPCTSTR	FindPreceedingWord(LPCTSTR cszStart, LPCTSTR cszColon);
};


